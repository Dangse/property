/** 빠른 조회를 위한 인덱스 */
export const LAW_INDEX = Object.fromEntries(LAW_DATABASE.map(l => [l.key, l]));

/**
 * 답변 텍스트에서 법령 인용 추출 (정규식)
 * 매칭 패턴: "법률명 제\d+조[의\d+]?[ ④⑤...]?"
 *
 * @param {string} text
 * @returns {string[]} 발견된 인용 키 (DB 형식: "소득세법 §95")
 */
export function extractCitations(text) {
  const found = new Set();

  // "소득세법 제95조", "상속세 및 증여세법 제53조의2" 등
  const re = /([가-힣]+(?:\s*및\s*[가-힣]+)?(?:\s*시행령)?)\s*제\s*(\d+)\s*조(?:\s*의\s*(\d+))?/g;
  let m;
  while ((m = re.exec(text)) !== null) {
    const lawName = m[1].replace(/\s+/g, ' ').trim();
    const article = m[3] ? `${m[2]}의${m[3]}` : m[2];
    const key = `${lawName} §${article}`;
    found.add(key);
  }

  return [...found];
}

/**
 * 인용 키들을 DB와 대조
 * @returns {Array<{key:string, status:'verified'|'unknown', entry?:object}>}
 */
export function checkCitations(citations) {
  return citations.map(key => {
    const entry = LAW_INDEX[key];
    return entry
      ? { key, status: 'verified', entry }
      : { key, status: 'unknown' };
  });
}
